/* Operadores de atribuição
   +=   --> Adição
   -=   --> Subtração
   *=   --> Multiplicação
   /=   --> Divisão
   %=   --> Resto

   */

let myNumber = 10;
myNumber = myNumber +2;
console.log(myNumber);

// para abreviar o ( myNumber = myNumber + 2 ), utilizamos operadores de atribuição  +=:

let myNumber2 = 10;
myNumber2 += 2;
console.log(myNumber2);

