// while( enquanto ) - Devemos usar o while quando não for previsível algum evento acontecer.
// Assim como no for, devemos informar a condição até quando ele vai se repetir. 

let i = 0;
while(i < 20) {   // executa enquanto for verdadeiro, ou seja, menor que 20.
    console.log(i)
    i++  //incrementa mais 1. Aqui equivale ao comando de incremento do for. nunca esquecer da condição.
}

let p = 0;
while(p < 10){
    console.log(p)
    p += 2  // vai imprimir de 2 em 2. Equivale ao comando de incremento do for. nunca esquecer da condição.
}

